        <h1>Strawberry Fields Forever: Growth through Acquisition</h1>
    </div>
    <section class="case-study">
        <p>Sunrise Growers and Frozsun Foods are based upon a vertically integrated business model in the fruit and vegetable space, processing fresh and frozen foods from field to fork.</p>
        <p>We developed an Advisory Board primarily to manage a growth strategy through acquisitions.  We assembled a seasoned group of experienced board members who provided different skills sets, yet could act as a team to support growth an provide the financing necessary to fund the strategy.</p>
        <p>With a well-defined plan, the senior management team was able to growth the business from $100 million to $300 million in sales through 6 significant acquisitions.  We were then able to conduct and evaluate how to achieve a liquidity event through taking the company to market and, ultimately, completed a private transaction with an East Coast private equity firm.</p>
        <p>Our Advisory Board was invaluable in the entire process, including providing guidance on working with the new private equity owners.  The transition from privately-owned to professionally-managed ownership was greatly enhanced by the Board’s input.  Our Board of Advisors was the key to our firm’s double-digit growth and, specifically, my development as a CEO during the period.</p>
        <p>My advice to business owners is that you cannot afford not to have a Board of experienced advisors who provide objective business advice and to be a though partner for the CEO.</p>
        <h5 class="author">Doug Circle, Founder and former CEO of Sunrise Growers and Frozsun Foods</h5>
