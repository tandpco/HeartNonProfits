        <h1>Metagenics</h1>
    </div>
    <section class="case-study">
        <p>Metagenics was a small (approximately $50 million), yet rapidly growing company. We were considering an equity transaction (IPO or private equity) to help us finance future growth and possible acquisitions.</p>
        <p>As an entrepreneur, founder and CEO I wanted to learn how to function under a Board of Directors and wanted to tap the expertise of senior executives who had already been through what I was considering for our Company. The Advisory Board option was perfect fit for our Company and me. It was very helpful for me in navigating a variety of difficult challenges, three acquisitions and two financings while growing the Company to revenue over $240 million.</p>
        <p>Working with one of the Founders of The Board Group I interviewed Advisory Board candidates and selected experience sets and personalities that I thought would be good fits for our business. Then we got to work. We met monthly for the first 3 months and then went to quarterly meetings. The Board would ask probing questions that helped me better think through complicated issues and come to better decisions. The Board interaction improved the depth of my thinking and that carried over to the day-to-day decision making.</p>
        <p>As an entrepreneur with many ideas, one of the most significant benefits of the Board was helping me stay focused. They helped us focus on the most important opportunities and continued to improve the focus on our strategic plan. That focused helped us make faster and more profitable progress. They also provided guidance and insight into the financial management of the Company, helping to improve profitably, guarding the integrity of the balance sheet and managing the relationships with our auditors. Their guidance on organizational development was also helpful in reaching our goals.</p>
        <p>Looking back, I feel that the most profound benefit of the Advisory Board was that it helped me grow my CEO skills and keep up with changing demands of a growing company. It was a good experience.</p>
        <br>
        <h5 class="author">Jeff Katke, CEO, Metagenics</h5>
