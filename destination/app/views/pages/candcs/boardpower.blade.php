        <h1>Board Power – Owner and Board Member Perspective</h1>
    </div>
    <section class="case-study">
    <p>I have experienced Advisory Boards from two perspectives. First, as owner/CEO of a rapidly growing privately-held company, I utilized an Advisory Board. Second, I have served as a member of a number of Advisory Boards. I have experienced the power of an Advisory Board as a beneficiary, as well as witnessed its impact as an Advisory Board member.</p>
    <p>I believe that one of the most important things an Advisory Board does is help the owner determine and clarify the outcomes he wants and then hone them into objectives with well-delineated plans for their achievement.</p>
    <p>The second thing an Advisory Board does to help create the outcomes the owners want and to provide an objective outside view of the Company.  Too often, the owner/CEO is too close to the business and the people in the business to access strengths and weaknesses.  Helping the owner see the company objectively through an experienced outsider’s perspective is essential to achieving the desired outcomes.</p>
    <p>Finally, Advisory Boards are typically composed of individuals with considerable business experience. The sharing of the lessons learned from their collective experience in invaluable in addressing issues and challenges as they arrive.</p>
    <p>I is clear to me that Advisory Boards can be tremendously impactful in helping owners achieve the outcomes they want, but they must be matched up with an owner/CEO who is open to challenging questions and insights and can translate these into sustainable effort to realize the full benefit of an Advisory Board.</p>
    <br>
    <h5 class="author">Keith Swayne, former owner and CEO of Case Swayne</h5>
