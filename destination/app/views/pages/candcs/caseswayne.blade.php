        <h1>Outside Thinking, Inside Culture and Security</h1>
    </div>
    <section class="case-study">
    <p>When I decided to have an Advisory Board, I looked for a group of people who could bring my management team and me different ways to think about the complicated challenges we faced. I wanted and needed different and diverse opinions that we were not always able to come up with. We are a close-knit management team. There was a danger that we could be predictable in our thinking and miss possible alternatives or make wrong decisions.</p>
    <p>I chose Board members who were sophisticated and worth of respect by our team.  With that respect, the Board could hold management accountable for the right decision processes and for following through on the actions committed to by my team. Each of the people selected had strong but different skills and experiences.  They each appreciated the Vistage process for conducting effective meetings. They know when to ask questions, when to give advice and when to pull me “out of the weeds”. It was very import to me that these outside people understood my values and style. They needed to know the culture we have at IMS so they could give good advice that fit my culture, which is so precious to me. Service to our customers is the highest priority; being a great place to work is second.</p>
    <p>Another reason to have this Board is to give my family and my employees the assurance that the Company would run well if I were not in the picture. If something happens to me, the Advisory Board becomes our Fiduciary Board. It has clear instructions on the steps to take to ensure that my estate and family realized the highest value for the company and that our employees are treated fairly.</p>
    <p>The Board gives me the security that helps my sleep a little better. I could not imagine running my business without having this group of people as my sounding board.</p>
    <br>
    <h5 class="author">Neil Sherman, Chairman and CEO of Industrial Metal Supply</h5>
